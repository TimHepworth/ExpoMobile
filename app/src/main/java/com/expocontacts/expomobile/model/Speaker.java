package com.expocontacts.expomobile.model;

public class Speaker {

    private Integer mSpeakerId;
    private String mSpeakerName;
    private String mJobTitle;
    private String mSpeakerCompany;
    private String mSpeakerBio;
    private String mEmailAddress;
    private String mSpeakerPhoto;

    public Integer getSpeakerId() {
        return mSpeakerId;
    }

    public void setSpeakerId(Integer speakerId) {
        mSpeakerId = speakerId;
    }

    public String getSpeakerName() {
        return mSpeakerName;
    }

    public void setSpeakerName(String speakerName) {
        mSpeakerName = speakerName;
    }

    public String getJobTitle() {
        return mJobTitle;
    }

    public void setJobTitle(String jobTitle) {
        mJobTitle = jobTitle;
    }

    public String getSpeakerCompany() {
        return mSpeakerCompany;
    }

    public void setSpeakerCompany(String speakerCompany) {
        mSpeakerCompany = speakerCompany;
    }

    public String getEmailAddress() {
        return mEmailAddress;
    }

    public void setEmailAddress(String emailAddress) {
        mEmailAddress = emailAddress;
    }

    public String getSpeakerBio() {
        return mSpeakerBio;
    }

    public void setSpeakerBio(String speakerBio) {
        mSpeakerBio = speakerBio;
    }

    public String getSpeakerPhoto() {
        return mSpeakerPhoto;
    }

    public void setSpeakerPhoto(String speakerPhoto) {
        mSpeakerPhoto = speakerPhoto;
    }

    public Speaker() {

    }

}
